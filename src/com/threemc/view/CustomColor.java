package com.threemc.view;

import java.awt.Color;


public class CustomColor {

	
	public static Color okColorBackGround() {
		Color okColor = Color.decode("#cdfdaf");
		return okColor;
	}
	
	public static Color notOkColorBackGround() {
		Color notOkColor = Color.decode("#fd9c9c");
		return notOkColor;
	}
	
	public static Color goldColor() {
		Color goldColor = Color.decode("#ffd700");
		return goldColor;
	}
	
	public static Color bgColor() {
		Color goldColor = Color.decode("#fbf7cd");
		return goldColor;
	}
	
}
